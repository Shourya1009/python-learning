# You cannot terminate a string literal in the next line without escape sequence , so for that we can use escape sequence to avoid that error

# To print a new line in the same string literal we use \n
print("Hello how are you ?\nI am good what about you ? ")


# To prove a tab space we use \t

print("Hello \t how are you ?")


# to print backslash  we use \\

print("I am going to print backslash \\ by using double backslash ")


# To print double quote within a string we use \"...\""

print("I will print \"double quotes\" within a string.")


# To print single quotes within a string  we use \'...\'

print("I will print \'single quotes\' within a string.")
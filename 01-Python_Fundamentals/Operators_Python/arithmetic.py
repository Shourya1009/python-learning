# Arithmetic Operators

a=34
b=2

print("a + b : ",a+b)   # addition
print("a - b : ",a-b)   # subtraction
print("a * b : ",a*b)   # multiplication
print("a / b : ",a/b)   # division - return quotinent as a float 
print("a % b : ",a%b)   # Modulus - returns remainder
print("a ** b : ",a**b)  # Exponentiation - return power of 34^2
print("a // b : ",a//b)  # Floor Division - returns the quotient as an integer 



# Logical Operator

a=True
b=False


# AND operator (and)
print("AND Operator return true if both the operands are true ")
print(a and a) 
print(a and b)
print(b and a)
print(b and b)



# Or operator (or)
print("Or Operator return true if either of the operands is true ")
print(a or a) 
print(a or b)
print(b or a)
print(b or b)



# Not Operator (not)

print("Not Operator return true if the operand is false ")
print(not(True))
print(not(False))
print(not(a))